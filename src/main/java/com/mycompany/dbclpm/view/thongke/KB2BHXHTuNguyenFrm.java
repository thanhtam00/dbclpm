    private void filltblNguyenLieu(ArrayList<Voluntary> list){
        model.setNumRows(0);
        int i = 1;
        for(Voluntary x: list){
//            System.out.println(x.toString());
            model.addRow(new Object[]{
                i++,
                x.getTime().toString(),
                x.getSalary(),
            });
        }
    }

