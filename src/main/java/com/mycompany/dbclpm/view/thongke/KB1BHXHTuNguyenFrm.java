    private void filltblNguyenLieu(ArrayList<Member> list){
        model.setNumRows(0);
        int i = 1;
        for(Member x: list){
//            System.out.println(x.toString());
            model.addRow(new Object[]{
                i++,
                x.getIdBHXH(),
                x.getName(),
                x.getIdNumber(),
                x.getBirthday(),
                x.getJoinDate(),
            });
        }
    }
    
